package operations;

public class IncrementDecrement {
	
	public static void main(String[] args) {
		
		
		int a=67;
		System.out.println(a);
		
		a++;  // post increment
		System.out.println(a);
		
		int b= 89;
		System.out.println(b);
		
		b--; // post decrement
		System.out.println(b);
		
		
		System.out.println("-----------------------------------");
		
		int c= 38;
		System.out.println(c);
		
		++c;  //pre increment
		System.out.println(c);
		
		int d=45;
		System.out.println(d);
		
		--d; // pre decrement
		System.out.println(d);
		
		
		
	}

}
